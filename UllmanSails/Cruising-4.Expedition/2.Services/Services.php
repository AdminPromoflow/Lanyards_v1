  <h2>Engineering for Extreme Environments</h2>
  <p>All Expedition Series sails are custom projects, constructed and designed with excruciating attention to detail to ensure durability and safety. The demands on crews in extreme cold, high winds, and big seas mean they must trust their gear. These sails allow crews to better manage high wind, sub-zero environments where mistakes quickly compound, and physical and mental responsiveness is impaired. </p>
  <p>High winds can cause the boat to heel substantially – risking the mainsail getting pulled into the water and potentially resulting in significant damage. Necessary sail changes can be a dangerous practice as well. The Expedition Series offers solutions that help reduce, or in some cases eliminate, the challenges that extreme environments pose – helping prepare you for the unknown.</p>

  <h2>Expedition Series Sail Options</h2>
  <ul>
    <li><strong>FiberPath Expedition: </strong>The sails of choice for Skip Novak. These FiberPath sails feature higher denier, increased fiber density, and greater sail film and taffeta weights.</li>
    <li><strong>Expedition Radial:</strong>These sails utilize a premium blend of woven spectra and dyneema cloth in a radial construction. The material, which features a polyester rip-stop weave, results in a fabric with a high-tear strength compared to other woven polyesters.</li>
    <li><strong>Expedition XC: </strong>These crosscut sails are built using heavy-duty, high-weight woven Dacron. Expedition XC sails are the sails of choice for the Barba Expedition team – taking them thousands of miles in the Arctic Circle, as far North as 81 degrees latitude.</li>
  </ul>
  <h2>Expedition Series Sail Design</h2>
  <p>Sail design for the Expedition Series is different. All Expedition Series sails are designed by the Ullman Sails Advanced Design Team, who consult with our Head of Technical Development and Skip Novak to address all the required standards. High clew heights in headsails offer increased visibility; mainsail reef heights that rise at the leech keep the boom clear of the water in breeze; and additional fiber placement in high-load areas of the sail offer more durability in extreme conditions. These and other design details improve sail handling and add safety to the boat and crew.</p>
